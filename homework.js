// 'number' + 3 + 3
// Конкатенація рядка 'number' з числами 3 і 3
console.log('number' + 3 + 3); // Результат: 'number33'

// null + 3
// Додавання null та числа 3
console.log(null + 3); // Результат: 3

// 5 && "qwerty"
// Логічне І: якщо обидва операнди істинні, повертається другий операнд
console.log(5 && "qwerty"); // Результат: "qwerty"

// +'40' + +'2' + "hillel";
// Унарний плюс перетворює рядок на число, потім відбувається додавання та конкатенація
console.log(+'40' + +'2' + "hillel"); // Результат: 42hillel

// '10' - 5 === 6;
// Вираз '10' - 5 дає 5, потім порівнюється з 6
console.log('10' - 5 === 6); // Результат: false

// true + false
// Перетворення булевих значень на числа та їх додавання
console.log(true + false); // Результат: 1

// '4px' - 3
// Рядок '4px' не може бути конвертований в число, тому отримаємо NaN (Not a Number)
console.log('4px' - 3); // Результат: NaN

// '4' - 3
// Рядок '4' перетворюється в число, потім відбувається віднімання
console.log('4' - 3); // Результат: 1

// '6' + 3 ** 0;
// Рядок '6' конкатенується з числом 3 в степені 0
console.log('6' + 3 ** 0); // Результат: '61'

// 12 / '6'
// Рядок '6' перетворюється в число, потім відбувається ділення
console.log(12 / '6'); // Результат: 2

// '10' + (5 === 6);
// Вираз (5 === 6) повертає false, потім відбувається конкатенація з рядком '10'
console.log('10' + (5 === 6)); // Результат: '10false'

// null == ''
// null рівний лише undefined, але не пустому рядку
console.log(null == ''); // Результат: false

// 3 ** (9 / 3);
// Піднесення 3 до степеня, яка обчислюється як 3 ** 3, що дорівнює 27
console.log(3 ** (9 / 3)); // Результат: 27

// !!'false' == !!'true'
// Обидва рядки перетворюються в true, оскільки вони не є пустими
// Потім двічі застосовується оператор НЕ, що повертає їх в початковий булевий вигляд
console.log(!!'false' == !!'true'); // Результат: true

// 0 || '0' && 1
// Логічне І: якщо перший операнд false, повертається його значення; інакше повертається другий операнд
console.log(0 || '0' && 1); // Результат: '0'

// (+null == false) < 1;
// Перетворення null в число дає 0, потім порівнюється з false (яке перетворюється в 0)
// Результат порівняння (0 == 0) буде true, а true перетворюється в 1
console.log((+null == false) < 1); // Результат: true

// false && true || true
// Логічне І: якщо перший операнд false, повертається його значення; інакше повертається другий операнд
// Логічне АБО: якщо перший операнд true, повертається його значення; інакше повертається другий операнд
console.log(false && true || true); // Результат: true

// false && (false || true);
// Спочатку відбувається операція в дужках, де вираз false || true повертає true
// Потім false && true повертає false
console.log(false && (false || true)); // Результат: false

// (+null == false) < 1 ** 5;
// Спочатку порівнюється null з false, результат порівняння (0 == 0) буде true
// Потім відбувається піднесення числа 1 до степеня 5
console.log((+null == false) < 1 ** 5); // Результат: true
